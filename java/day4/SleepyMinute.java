public class SleepyMinute {
    private int minute;
    private int occurrences;

    public int getMinute() {
        return this.minute;
    }

    public int getOccurences() {
        return this.occurrences;
    }

    public SleepyMinute(int minute, int occurrences) {
        this.minute = minute;
        this.occurrences = occurrences;
    }
}